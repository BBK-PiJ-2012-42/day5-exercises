
package day5;



public class Calculator {
    public void add(int a, int b) {
        double c = a, d = b;
        double output = 0;
        output = (c+d);
        System.out.println(output);
    }

    public void subtract(int a, int b) {
        double c = a, d = b;
        double output = 0;
        output = (c-d);
        System.out.println(output);
    }

    public void multiply(int a, int b) {
        double c = a, d = b;
        double output = 0;
        output = (c*d);
        System.out.println(output);
    }

    public void divide(int a, int b) {
        double c = a, d = b;
        double output = 0;
        output = (c/d);
        System.out.println(output);
    }
}
